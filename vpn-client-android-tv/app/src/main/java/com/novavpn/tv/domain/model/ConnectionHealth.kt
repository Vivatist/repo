package com.novavpn.tv.domain.model

/**
 * Уровень здоровья VPN-соединения.
 * Определяется пассивным мониторингом: сколько прошло с момента
 * последнего полученного пакета от сервера.
 */
enum class ConnectionHealth {
    /** Связь стабильна (< 35 сек без пакетов). */
    GOOD,
    /** Возможна потеря пакетов (35-60 сек). */
    DEGRADED,
    /** Связь потеряна (> 60 сек), инициируется reconnect. */
    LOST;

    fun displayName(): String = when (this) {
        GOOD -> "Стабильно"
        DEGRADED -> "Нестабильно"
        LOST -> "Потеряно"
    }
}
